public class q24 {
    public static void main(String[] args) {
        // how many of the groups of three numbers in the list are pythagorean triples?
        int[][] possTriples = { { 5239, 13800, 14761 }, { 940, 23293, 29121 }, { 6814, 23248, 31029 },
                { 10944, 1890, 11106 }, { 9240, 6262, 11162 }, { 1036, 23138, 29000 }, { 1155, 23294, 30716 },
                { 9998, 23270, 26067 }, { 9672, 10246, 14090 }, { 3016, 23185, 30727 }, { 7497, 10504, 12905 },
                { 6747, 14204, 15725 }, { 8176, 9090, 12226 }, { 5161, 23238, 26812 }, { 7304, 12810, 14746 },
                { 11136, 2120, 11336 }, { 10012, 23297, 31554 }, { 8624, 10290, 13426 }, { 3679, 23303, 33041 },
                { 798, 23289, 29670 }, { 10057, 2424, 10345 }, { 9028, 23295, 26854 }, { 1531, 23149, 27328 },
                { 10235, 2652, 10573 }, { 6478, 23285, 32381 }, { 4435, 23250, 27401 }, { 10241, 5880, 11809 },
                { 9595, 23141, 32042 }, { 10087, 5616, 11545 }, { 1899, 23174, 30526 }, { 4130, 23272, 30949 },
                { 1065, 22672, 22697 } };
        int count = 0;
        for (int i = 0; i
                < possTriples.length; i++) {
            if (Math.pow(possTriples[i][0], 2)
                    + Math.pow(possTriples[i][1], 2)
                    == Math.pow(possTriples[i][2], 2)) {
                count++;
            }
        }
        System.out.println(count);
    }
}
